# ------------------------------------------------
# Understanding Basic Syntax and Variable Declarations
# ------------------------------------------------

# Hello, World!
print("Hello, World")
print("I am diving into Python for Data Engineering.")

# Defining a variable and its types
data_engineer_experience = 3  # Numeric variable representing years of experience
data_engineer_name = "Ansh"  # String variable storing the name of the data engineer

# Printing variable values
print("Data Engineer:", data_engineer_name)
print("Experience (years):", data_engineer_experience)

# ----------------------------------------------
# Data Types: Numeric, String, and Boolean
# ----------------------------------------------

# Numeric variables
number_of_servers = 10
average_response_time = 25.5

# String variables
data_pipeline_name = "ETL_Pipeline"
database_table_name = "customer_data"

# Boolean variable
is_data_clean = True

# Printing information about the data
print("Number of Servers:", number_of_servers)
print("Average Response Time:", average_response_time)
print("Data Pipeline Name:", data_pipeline_name)
print("Database Table Name:", database_table_name)
print("Is Data Clean?", is_data_clean)

# ----------------------------------------------
# Operators: Arithmetic, Comparison, Logical
# ----------------------------------------------

# Arithmetic operators
addition_result = number_of_servers + 5
multiplication_result = average_response_time * 2

# Comparison operators
is_pipeline_matched = data_pipeline_name == "ETL_Pipeline"
is_experience_greater = data_engineer_experience > 2

# Logical operators
is_data_ready = is_data_clean and is_pipeline_matched

# Printing results of operators
print("Addition Result:", addition_result)
print("Multiplication Result:", multiplication_result)
print("Is Pipeline Matched?", is_pipeline_matched)
print("Is Experience Greater than 2?", is_experience_greater)
print("Is Data Ready for Analysis?", is_data_ready)

# ----------------------------------------------
# Conclusion
# ----------------------------------------------

